package h_literals;

public class c_boolean_literals {
    public static void main(String[] args) {
        boolean Ismale = false;
        String name = "Suman";
        if(Ismale == true)
        {
            System.out.println("Hello Mr. " + name);
        }
        else
        {
            System.out.println("Hello Ms. " + name);
        }
    }
}
