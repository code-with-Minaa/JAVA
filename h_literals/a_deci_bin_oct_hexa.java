package h_literals;

public class a_deci_bin_oct_hexa {
    public static void main(String[] args) {
        int num_decimal=23;
        int num_binary = 0b10110;
        int num_octal = 0110;
        int num_hexadecimal = 0xA2;
        System.out.println("The decimal value = " + num_decimal);
        System.out.println("The binary value = " + num_binary);
        System.out.println("The octal value = " + num_octal);
        System.out.println("The hecadecimal value = " + num_hexadecimal);
        
    }
}
 